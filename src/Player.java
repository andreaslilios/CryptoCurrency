
public class Player {

    protected String name;
    protected int sum;      // gia thn vathmologia

    public Player() {
        this.name = name;
        this.sum = sum;
    }

    public void setSum(int sum) {
        this.sum = sum;
    }

    public String getName() {
        return name;
    }

    public void setName(String n) {
        this.name = n;
    }

    public int getSum() {
        return sum;
    }

}
